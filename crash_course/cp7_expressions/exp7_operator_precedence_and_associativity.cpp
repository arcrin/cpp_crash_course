/*
 * When more than one operator appears in an expression, operator precedence and operator associativity decide how
 * the expression parses. Operators with higher precedence are bound tighter to their arguments than operators with
 * lower precedence. If two operators have the same precedence, their associativity breaks the tie to decide how
 * arguments bind. Associativity is either left to right or right to left
 */